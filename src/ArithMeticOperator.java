public class ArithMeticOperator {

    short a = 4;
    short b = 5;
    public int add() {
        return a + b;
    }

    public int subtract() {
        return a - b;
    }

    public int multiply() {
        return a*b;
    }

    public int divide() {
        return a/b;
    }

    public int modulus() {
        return a % b;
    }
}
